import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages, behaviorContext } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are an empathetic NLP-powered mood analyst integrated into the AETHER Emotion Drift Engine dashboard. Your role:

1. MOOD DETECTION: Analyze the user's text for emotional cues and detect their current mood. Classify into: happy, sad, anxious, stressed, calm, excited, frustrated, neutral, angry, hopeful.
2. MOOD SCORE: Rate the emotional valence from -1.0 (very negative) to +1.0 (very positive).
3. BEHAVIOR CONTEXT: When behavior data is provided (keystrokes, clicks, scrolls, intensity), incorporate it into your analysis. Rapid typing bursts may indicate urgency/stress. Long pauses may indicate contemplation or fatigue.
4. RESPONSE FORMAT: Always respond with a JSON block at the START of your message wrapped in \`\`\`json ... \`\`\` containing:
   { "mood": "detected_mood", "score": 0.5, "confidence": 0.85 }
   Then follow with a natural, empathetic response.
5. Be conversational, supportive, and insightful. Reference patterns you notice across the conversation.

${behaviorContext ? `Current behavior context: ${JSON.stringify(behaviorContext)}` : ''}`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please wait a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI usage limit reached. Please add credits." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("mood-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
